package ch5;

public class Switch {
    public static void main(String[] args){
        //얼짱뮤지션
        //걸어서 하늘까지() - 걸리면 break 까지
        int n = 3;

        switch (n){
            case 1:
                System.out.println("SIMPLE JAVA");
            case 2:
                System.out.println("FUNNY JAVA");
            case 3:
                System.out.println("FANTASTIC JAVA");
            default:
                System.out.println("THE BEST PROGRAMMING LANGUAGE");
        }

    }
}
